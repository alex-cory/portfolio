export default class MediumPost {
  constructor(id, title, date, url, content, image, likes, name, username) {
    this.id = id
    this.title = title
    this.date = date
    this.url = url
    this.content = content
    this.image = image
    this.likes = likes
    this.name = name
    this.username = username
  }
}